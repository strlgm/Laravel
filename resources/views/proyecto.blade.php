<p>Este es un proyecto nacido de la necesidad detectada en los niños de la Comuna 10 en Pasto, Colombia. </p>
<u> <H2>Nuestro Objetivo</H2> </u>
<p>Lograr que los niños y niñas de la comuna 10 generen auto-reconocimiento y empatía entre ellos a través de expresiones audivisuales.</p>

<u> <H2> ¿Como lo logramos? </H2></u>
<p>Utilizamos la Metodología de Vídeo participativo para generar la participación en las comunidades, con esto logramos que los niños puedan sentirse parte de la comunidad.</p>

<p>Dentro del Kit La Ventana, incluimos diferentes artículos que le permitirán realizar la grabación de videos, de la misma forma se incluyen tarjetas de acción que funcionan como guia para elegir el tema, como y donde grabar el video. Esto permite que se ha de manera mas ludica para los niños. </p>

